package Q_128_156;

class Fieldint{
	char c;
	boolean b;
	float f;
	void printAll() {
		System.out.println("c= "+c);//c=
		System.out.println("b= "+b);//b=false
		System.out.println("f= "+f);//f=0.0
	}
	
}

public class Q147 {
	
	
	public static void main(String[] args) {
		Fieldint f=new Fieldint();
		f.printAll();
	}

}
